                                <p>進入江戶時代，飲茶文化已經普遍，但為了讓茶飲文化更加大眾化，發展出了煎茶，而成為現今最受歡迎的日本茶。但無論是抹茶或是煎茶，都無疑是日本兩條主要的飲茶文化。</p>
