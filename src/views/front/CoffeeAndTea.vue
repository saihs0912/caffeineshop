                                <p>茶文化融入現代台灣社會外，也逐漸進步，發展出各種不同的口味，連鎖茶飲店在台灣大街小巷林立，最知名的即是珍珠奶茶，現在台灣也以此在國際打響名號。</p>
                                <p><span class="fw-bold fs-4">日本：</span>西元8世紀奈良時代，遣唐使與留學僧侶至當時的唐朝學習文化，將茶文化帶回日本，開啟日本的飲茶文化。即便當時主要是供貴族及僧侶飲用。後來至鎌倉時代，又再度派遣留學僧侶至當時的宋朝學習，將當時宋的茶文化帶回日本，成為日本茶道的起源。</p>
                                <p>唐代時流行「煮茶法」，把茶葉煮成湯，加薑、蔥、鹽調味。至宋代時開始流行「點茶法」，將茶葉弄成類似茶磚的外貌後，飲用時將茶磚弄碎磨成粉，泡入熱水飲用。而這就是日本抹茶的祖先。</p>
                                <div class="float-lg-end float-md-end floatImg ps-2 pb-4">
