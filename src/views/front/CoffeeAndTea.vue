                                <p>17世紀初，荷蘭東印度公司引進第一批茶葉至荷蘭，很快成為該國上流社會的飲品，並從此傳播到西歐其他國家。</p>
                                <p>葡萄牙也是早期接觸中國茶葉的國家之一，傳教士與商人將茶葉傳入葡萄牙後，將飲茶文化帶入宮廷。1662年，葡萄牙凱薩琳公主與英國國王查理二世成婚，並將其飲茶習慣帶至英國皇室，至此茶飲文化正式打入英國的貴族圈內。</p>
