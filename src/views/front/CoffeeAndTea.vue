                                <p>19世紀，英國開始流行下午茶文化，在午後享用茶點及輕食，成為當時英國上流社會的的流行。</p>
                                <p>在英國茶飲文化流行的同時，也意識到了茶葉的生產都由當時清朝所壟斷。為了打破壟斷（以及改變貿易逆差），當時的英國政府與東印度公司策劃了一場商業間諜行動，派遣蘇格蘭植物學家羅伯特·福鈞，偽裝成清國商人進入內陸地區。竊取茶種子及幼苗，以及製茶工藝。接著將技術與茶種跟茶苗帶至英屬印度，種植於大吉嶺及阿薩姆，成為現今孰悉的紅茶產地。這也是歷史上有名的偷茶事件。</p>
                                <p>除了英國的紅茶外，德國亦發展出了水果茶的文化，歐洲中部嚴冬漫長，新鮮水果難以保存，因此將水果烘乾後，放入熱水沖泡，如此一來即便在冬天，也可以嘗到水果的風味。所以德國也是水國茶主要生產國，生產原料通常包括芙蓉花、薔薇果、橙皮和蘋果片，這些都是歐洲中部常見的植物。</p>
