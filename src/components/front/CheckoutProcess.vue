            <div class="w-25 text-center align-self-center">
              <i class="bi bi-check-circle bg-white fs-1 text-success" v-if="step === 1"></i>
              <i class="bi bi-circle-fill bg-white fs-4 text-success" v-else-if="step > 1"></i>
            </div>
